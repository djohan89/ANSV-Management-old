package vn.ansv.Dto.Menu;

public class MenuProjectTypesDto {
	
	private int id;
	private String name;
	private int number;
	
	public MenuProjectTypesDto() {
		super();
	}

	public int getId() {
		return id;
	}
	
	public void setId(int id) {
		this.id = id;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}

	public int getNumber() {
		return number;
	}

	public void setNumber(int number) {
		this.number = number;
	}
	
}
